import sqlite3

# Connect to database
conn = sqlite3.connect('booking_system_v2.db')
cursor = conn.cursor()

for activity_name, details in professional_activities.items():
    description = details['description']
    category = details['category']
    
    try:
        # Update activity description
        cursor.execute("""
            UPDATE Activity 
            SET description = ?
            WHERE name = ?
        """, (description, activity_name))
        
        if cursor.rowcount > 0:
            print(f"✅ Updated: {activity_name}")
            print(f"   Category: {category}")
            print(f"   Length: {len(description)} characters")
        else:
            print(f"⚠️  Activity not found: {activity_name}")
    except Exception as e:
        print(f"❌ Error updating {activity_name}: {e}")

conn.commit()
conn.close()

